<h4><b>Prevention of cervical cancer</b></h4>
<ol>
<li><b>Human papillomavirus (HPV) vaccine</b></li>
    <p> 
    The link between the development of cervical cancer and some types of HPV is clear. If every female adheres to current HPV vaccination programs, cervical cancer could potentially be reduced.</p>
<li><b>Safe sex and cervical cancer </b></li>
    <p>The HPV vaccine only protects against two HPV strains. There are other strains which can cause cervical cancer. Using a condom during sex helps protect from HPV infection.</p>
<li><b>Cervical screening</b></li> 
     <p>Regular cervical screening will make it much more likely that signs of cancer are picked up early and dealt with before the condition can develop, or spread too far. Screening does not detect cancer but detects changes to the cells of the cervix.</p>
<li><b>Having fewer sexual partners</b></li> 
     <p>The more sexual partners a woman has, the higher the risk of transmitting the HPV virus, which can lead to a higher likelihood of developing cervical cancer.</p>
<li><b>Delaying first sexual intercourse</b></li> 
    <p>The younger a female is when she has her first sexual intercourse, the higher the risk of HPV infection. The longer she delays it, the lower her risk.</p>
<li><b>Stopping smoking </b></li>
    <p>Women who smoke and are infected with HPV have a higher risk of developing cervical cancer than people who do not.</p>

    </ol> 

    

