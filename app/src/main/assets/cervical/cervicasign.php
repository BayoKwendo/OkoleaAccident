<h4><b>What are the symptoms and signs of cervical cancer?</b></h4>
<ul>
<li>Abnormal vaginal bleeding</li>
<li>Vaginal bleeding after menopause</li>
<li>Vaginal bleeding after sex</li>
<li>Bleeding or spotting between periods</li>
<li>Longer or heavier menstrual periods than usual</li>
<li>Other abnormal vaginal discharge</li>
<li>Pain during sexual intercourse</li>
<li>Smelly vaginal discharge</li>
<li>vaginal discharge tinged with blood</li>
<li>pelvic pain</li>
</ul>
<p><b>N/B</b></p>
