<h3><b>WHAT IS CERVICAL CANCER</b></h3>
<p><b>Cervical cancer</b> is cancer that begins in the uterine cervix, the lower end of the uterus that contacts the upper vagina.</p>
<p>Cervical cancer remains a common cause of cancer and cancer death in women in developing countries without access to <b>screening (Pap testing)</b> for cervical cancer or vaccines against <b>human papillomaviruses (HPVs).</b>
<p>Cervical cancer is different from cancer that begins in other regions of the uterus (<b>uterine or endometrial cancer</b>). </p>