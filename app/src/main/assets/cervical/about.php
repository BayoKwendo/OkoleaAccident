<p><b>ZeroCancer App</b> is an android application that aims at eradicating high numbers of cancer cases to zero, which will in turn save a lot money for the government leading to improving affordable high-quality health services.</p>
<h4><b>Statement of Problem</b></h4>
<p>To start with, Ing Kenya alone, more than 28,500 people die of cancer every year â€” about 78 people daily. A cancer test costs about Sh13,000 in the country. Low- and middle-income earners, the largest segment of the population, cannot afford to pay such an amount to get tested. Add to that the cost of treatment, palliative care and transport fare to the hospital.</p>
<p>Secondly, Many Kenyans are still in dark, they neither know what cancer is, nor how best they can contribute towards zero infections.</p>

<h4><b>Objectives</b></h4>
<ul>

<li>     Bring out an informed Society by giving out factual information to create awareness of cancer (Focused on Cervical). </li>
<li>	To Educate, Equip, and Empower People by providing a learning platform in form of an online forum.</li>
<li>	Provide Private Consultation with the Health Professional.</li>
<li>	Assist in automatic locating nearby health facilities in regardless of your location, provide details and help you in establishes the best route to use.</li>
<li>	Encouraged people to get screened and be enrolled in NHIF.</li>
</ul>
