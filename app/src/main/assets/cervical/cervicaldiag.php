<h5><b>In general, the stages of cervical cancer are as follows:</b></h5>

  <p><b>Stage 0:</b> This stage is not a true invasive cancer. The abnormal cells are only on the surface of the cervix, as in <b>CIN 3.</b> This stage is not included in the FIGO system and is referred to as <b>carcinoma in situ (CIS).</b></p>
  <p><b>Stage I:</b>There is a small amount of tumor present that has not spread to any lymph nodes or distant sites.</p>
  <p><b>Stage II:</b> The cancer has spread beyond the cervix and uterus, but does not invade the pelvic walls or the lower part of the vagina.</p>
  <p><b>Stage III:</b> The cancer has grown into the lower part of the vagina or the walls of the pelvis. The tumor may be blocking the ureters (tubes that carry urine from the kidneys to the bladder). There is no spread to other sites in the body.</p>
  <p><b>Stage IV:</b> This is the most advanced stage, in which the cancer has spread to the bladder or rectum, or to sites in other areas of the body.</p>


  <h4><b>Cervical cancer diagnosis and tests</b></h4>
  <p><b>HPV DNA testing</b></p>
   <P>This test determines whether the patient is infected with any of the HPV types that are the most 
   <P>likely to cause cervical cancer.</P>
   <p>The test can detect high-risk HPV strains in cell DNA before any cervix cell abnormalities appear.</p>
   <p>If a person experiences signs and symptoms of cervical cancer, or if the Pap test reveals abnormal cells, they might then undergo additional tests including:</p>

   <ol>
  <li><b>Biopsy</b> - a small section of tissue is taken under general anesthesia.</li>
   <li><b>Colposcopy</b> - a speculum is placed to hold the vagina open as the gynecologist looks at the cervix through a colposcope - a lighted magnifying instrument.</li>
   <li><b>Cone biopsy</b> - small cone-shaped section of abnormal tissue is taken from the cervix for examination.</li>
   <li><b>LLETZ</b> - diathermy (wire loop with an electric current) is used to remove abnormal tissue. The tissue is sent to the lab to be checked.</li>
   <li><b>Blood tests</b> - measure the number of blood cells, and can identify any liver or kidney problems.</li>
   <li><b>Examination under anesthesia (EUA)</b> - this allows the doctor to examine the vagina and cervix more thoroughly.</li>
   <li><b>CT scan</b> - a person consumes a barium drink that appears white on the scan. Just before the scan, a tampon can be placed into the vagina, and a barium liquid may be inserted into the rectum.</li>
   <li><b>MRI</b> - by using high-MRI with a special vaginal coil, (a technique to measure the movement of water within tissue), researchers may be able to identify cervical cancer in its early stages.</li>
