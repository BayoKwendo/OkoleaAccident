<p><b>Cervical cancer</b> treatment options include <b>surgery, radiotherapy, chemotherapy, or combinations of these.</b></p>
 <p><b>Treatment for early stage cervical cancer</b><p>
  <p>- cancer that is confined to the cervix - has a good success rate.</p>
<p><b>Surgery</b> is commonly used when the cancer is confined to the cervix.</p>
<p><b> Radiotherapy</b> may be used after surgery if a doctor believes there may still be cancer cells inside the body and also reduce the risk of recurrence (cancer coming back).</p>

<p><b>Treatment for advanced cervical cancer</b></p>
<p>In the later stages of cancer, <b>palliative therapy</b> is administered to relieve symptoms and improve quality of life.</p>
<ol>
<li><b>Radiotherapy</b></li>
<p><b>Radiotherapy</b> is commonly used to treat advanced forms of cervical cancer.</p>
<p>Radiotherapy is also known as <b>radiation therapy, radiation oncology, and XRT.</b></p>
<p> It involves the use of beams of high-energy X-rays or particles (radiation) to destroy cancer cells.</p>
<p> Radiation that is aimed at the pelvic area may cause the following side effects, some of which may not emerge until after the treatment is over:
  
  <ul>
  <li>diarrhea</li>
  <li>nausea</li>
  <li>upset stomach</li>
  <li>bladder irritation</li>
  <li>narrowing of the vagina</li>
  <li>interrupted menstrual cycle</li>
  <li>early menopause</li>
  </ul>
  </p>
  <li><b>Chemotherapy</b></li>
     <p><b>Chemotherapy</b> is the use of chemicals (medication) to treat any disease. In this context, it refers to the destruction of cancer cells.</p>
  
  <p>Side effects of chemotherapy can vary, and they depend on the specific drug being used. Below is a list of the more common side effects:
  <ul>
<li>diarrhea</li>
<li>nausea</li>
<li>hair loss</li>
<li>fatigue</li>
<li>infertility</li>
<li>early menopause</li>
</ul>
</p>
</ol>